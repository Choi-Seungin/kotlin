fun main() {
    var num = 10;
    /*
    복합대입 연산자 : 특정변수를 데이터에 계속 연산하여 누적할 때
    */

    num += 5 //num = num + n
    println(num)

    num -= 3 //num = num - n
    println(num)

    num *= 2 //num = num * n
    println(num)

    num /= 5 //num = num / n
    println(num)

    num %= 3 //num = num % n
    println(num)
    println(-num)
}